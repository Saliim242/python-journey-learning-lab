# Arithmetic Operators
# Arithmetic operators are used to perform arithmetic operations on numbers.
# The following arithmetic operators are supported in Python:
# 1. Addition (+)
# 2. Subtraction (-)
# 3. Multiplication (*)
# 4. Division (/)
# 5. Modulus (%)
# 6. Exponentiation (**)
# 7. Floor Division (//)

# Example
a = 10
b = 3
# Addition
addition = a + b
print("Addition:", addition)  # Output: 13
# Subtraction
subtraction = a - b
print("Subtraction:", subtraction)  # Output: 7
# Multiplication
multiplication = a * b
print("Multiplication:", multiplication)  # Output: 30
# Division
division = a / b
print("Division:", division)  # Output: 3.3333333333333335
# Modulus
modulus = a % b
print("Modulus:", modulus)  # Output: 1
# Exponentiation
exponentiation = a ** b
print("Exponentiation:", exponentiation)  # Output: 1000
# Floor Division
floor_division = a // b
print("Floor Division:", floor_division)  # Output: 3


# Comparison Operators
# Comparison operators are used to compare two values.
# The following comparison operators are supported in Python:
# 1. Equal to (==)
# 2. Not equal to (!=)
# 3. Greater than (>)
# 4. Less than (<)
# 5. Greater than or equal to (>=)
# 6. Less than or equal to (<=)

# Example
a = 10
b = 3
# Equal to
equal_to = a == b
print("Equal to:", equal_to)  # Output: False
# Not equal to
not_equal_to = a != b
print("Not equal to:", not_equal_to)  # Output: True
# Greater than
greater_than = a > b
print("Greater than:", greater_than)  # Output: True
# Less than
less_than = a < b
print("Less than:", less_than)  # Output: False
# Greater than or equal to
greater_than_or_equal_to = a >= b
print("Greater than or equal to:", greater_than_or_equal_to)  # Output: True

# Less than or equal to
less_than_or_equal_to = a <= b
print("Less than or equal to:", less_than_or_equal_to)  # Output: False

# Assignment Operators
# Assignment operators are used to assign a value to a variable.
# The following assignment operators are supported in Python:
# 1. Assignment (=)
# 2. Add and assign (+=)

# 3. Subtract and assign (-=)
# 4. Multiply and assign (*=)
# 5. Divide and assign (/=)
# 6. Modulus and assign (%=)
# 7. Exponentiation and assign (**=)
# 8. Floor division and assign (//=)

# Example
a = 10
# Assignment
b = a
print("Assignment:", b)  # Output: 10
# Add and assign

a += 5
print("Add and assign:", a)  # Output: 15
# Subtract and assign
a -= 5
print("Subtract and assign:", a)  # Output: 10
# Multiply and assign
a *= 5
print("Multiply and assign:", a)  # Output: 50
# Divide and assign
a /= 5
print("Divide and assign:", a)  # Output: 10.0
# Modulus and assign
a %= 3
print("Modulus and assign:", a)  # Output: 1.0
# Exponentiation and assign
a **= 3
print("Exponentiation and assign:", a)  # Output: 1.0
# Floor division and assign
a //= 3
print("Floor division and assign:", a)  # Output: 0.0


# Logical Operators
# Logical operators are used to combine conditional statements.
# The following logical operators are supported in Python:
# 1. and
# 2. or
# 3. not
# Example
a = True
b = False
# and
logical_and = a and b
print("Logical AND:", logical_and)  # Output: False
# or
logical_or = a or b
print("Logical OR:", logical_or)  # Output: True
# not
logical_not = not a
print("Logical NOT:", logical_not)  # Output: False


# Identity Operators
# Identity operators are used to compare the memory locations of two objects.
# The following identity operators are supported in Python:
# 1. is
# 2. is not
# Example
a = [1, 2, 3]
b = a
c = a.copy()
# is
identity_is = a is b
print("Identity is:", identity_is)  # Output: True
# is not
identity_is_not = a is not b
print("Identity is not:", identity_is_not)  # Output: False
# is
identity_is = a is c
print("Identity is:", identity_is)  # Output: False
# is not
identity_is_not = a is not c
print("Identity is not:", identity_is_not)  # Output: True

# Membership Operators
# Membership operators are used to test if a value is a member of a sequence.
# The following membership operators are supported in Python:
# 1. in
# 2. not in
# Example
a = [1, 2, 3]
b = 2
# in
membership_in = b in a
print("Membership in:", membership_in)  # Output: True
# not in
membership_not_in = b not in a
print("Membership not in:", membership_not_in)  # Output: False
